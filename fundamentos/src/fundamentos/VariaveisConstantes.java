package fundamentos;

public class VariaveisConstantes {
    public static void main(String[] args) {

        int numeroInt = 8;
        long numeroLong = 4L;
        double numeroDouble = 2.5;

        char caracterChar = 'a';
        String palavraString = "Karol";
        boolean isVerdadeiro = true;
        boolean isFalse = false;

        System.out.println(numeroInt);
        System.out.println(numeroLong);
        System.out.println(numeroDouble);
        System.out.println(caracterChar);
        System.out.println(palavraString);
        System.out.println(isVerdadeiro);
        System.out.println(isFalse);
    }
}
